<h2 class="mb-4">Sobre este portal web</h2>

<p>¡Hola! Este portal lo hice con PHP y usé <strong>Bootstrap 5</strong> para que se vea bien en cualquier dispositivo, ya sea celular, tablet o computadora.</p>

<h4>¿Por qué Bootstrap?</h4>
<ul>
    <li>Porque es fácil de usar y trae muchas cosas listas, como botones, menús y tablas, que me ayudaron a armar la página rápido.</li>
    <li>Además, con Bootstrap no tuve que preocuparme porque se viera raro en pantallas pequeñas, ya que es responsive automáticamente.</li>
    <li>También me gusta porque es muy popular y se usa mucho en la vida real, así que aprendí algo que me va a servir en otros proyectos.</li>
</ul>

<h4>¿Qué puedes hacer aquí?</h4>
<p>En este portal puedes probar varias APIs que muestran cosas interesantes, como:</p>
<ul>
    <li>Predecir el género y la edad de una persona solo con poner su nombre.</li>
    <li>Buscar universidades de cualquier país.</li>
    <li>Ver el clima en distintas ciudades de República Dominicana.</li>
    <li>Conocer datos de tus Pokémon favoritos, con imagen y hasta su sonido.</li>
    <li>Leer las últimas noticias de WordPress.</li>
    <li>Convertir dólares a otras monedas como pesos dominicanos o euros.</li>
    <li>Buscar imágenes bonitas escribiendo solo una palabra.</li>
    <li>Obtener información de países, como su bandera y capital.</li>
    <li>Y para relajarte un rato, un generador de chistes para que te diviertas.</li>
</ul>

<p>Intenté que todo fuera fácil de usar y que si algo falla o escribes algo mal, te salga un mensaje claro para que sepas qué pasó.</p>

